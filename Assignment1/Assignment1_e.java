package com.assignment;

public class Assignment1_e {

	public static void main(String[] args) {
		int a = 4;
		int b=8;
		System.out.println("Numbers before swapping are a=" + a + " and b="+ b +".");
		int swap = a;
		a=b;
		b=swap;
		
		System.out.println("Numbers after swapping are a=" + a + " and b="+ b +".");
	}
}
